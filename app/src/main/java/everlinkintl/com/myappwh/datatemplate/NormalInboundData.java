package everlinkintl.com.myappwh.datatemplate;

public class NormalInboundData {
    private String goods_name;
    private String remain_in_qty;
    private String lot1;
    private String pan_size;

    public String getGoods_name() {
        return goods_name;
    }

    public void setGoods_name(String goods_name) {
        this.goods_name = goods_name;
    }

    public String getRemain_in_qty() {
        return remain_in_qty;
    }

    public void setRemain_in_qty(String remain_in_qty) {
        this.remain_in_qty = remain_in_qty;
    }

    public String getLot1() {
        return lot1;
    }

    public void setLot1(String lot1) {
        this.lot1 = lot1;
    }

    public String getPan_size() {
        return pan_size;
    }

    public void setPan_size(String pan_size) {
        this.pan_size = pan_size;
    }
}
